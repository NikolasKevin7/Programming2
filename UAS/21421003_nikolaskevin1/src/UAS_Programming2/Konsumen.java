    private void BTNSAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNSAVEActionPerformed
        // TODO add your handling code here:
        try {
            Class.forName("com.mysql.jdbc.Driver");
            url="jdbc:mysql://"+ host +"/"+ db + "?user=" + user + "&password="+ pswd;
            Connection conn=DriverManager.getConnection(url);
            PreparedStatement pStatement = conn.prepareStatement("INSERT INTO tb_konsumen(nik,nama,jenismember,potongan)" + "VALUES (?,?,?,?)");

            pStatement.setString(1, TXTNIK.getText());
            pStatement.setString(2, TXTNAMA.getText());
            pStatement.setString(3, (String) CBJNSMEMBER.getSelectedItem());
            pStatement.setString(4, TXTPOT02.getText());

            if (pStatement.executeUpdate()>0)
            JOptionPane.showMessageDialog(this,
                "Penambahan sukses", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);
            else
            JOptionPane.showMessageDialog(this,
                "Penambahan gagal", "Informasi",
                JOptionPane.INFORMATION_MESSAGE);

            pStatement.close();
            conn.close();

            TXTNIK.setText("");
            TXTNAMA.setText("");
            CBJNSMEMBER.setSelectedItem("");
            TXTPOT02.setText("");

        }
        catch(ClassNotFoundException e) {
            System.out.println("jdbc.Driver tidak ditemukan");
        }

        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_BTNSAVEActionPerformed

