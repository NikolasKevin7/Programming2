    private void BTNCEKNIKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNCEKNIKActionPerformed
        // TODO add your handling code here:
        try {
            Statement st = Lconnection.createStatement();
            ResultSet rs = st.executeQuery (" Select * from tb_cetakreport" + "where nik like('%" + TXTNIK.getText() + "%')");
            if (rs.next()) {
                BTNEDIT.setEnabled(true);
                TXTNIK.setText(rs.getString("nik"));
                TXTNAMA.setText(rs.getString("nama"));
                CBJNSMEMBER.setSelectedItem(rs.getString("jenismember"));
                TXTPOT02.setText(rs.getString("potongan"));
            } else {
                JOptionPane.showMessageDialog(this,"ID: Salah", "Informasi", JOptionPane.INFORMATION_MESSAGE);

                BTNEDIT.setEnabled(false);
                TXTNIK.setText("");
                TXTNAMA.setText("");
                CBJNSMEMBER.setSelectedItem("");
                TXTPOT02.setText("");
                TXTNAMA.requestFocus();
            }
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_BTNCEKNIKActionPerformed

