    private void btnproses1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproses1ActionPerformed
        // TODO add your handling code here:
       String kodepelanggan,status;
       double biayadaftar;

        kodepelanggan=String.valueOf(txtkodepelanggan.getText());
        biayadaftar=Double.parseDouble(txtbiayadaftar.getText());

        if(kodepelanggan.equals("AAAA")){
            status ="BARU";
            biayadaftar =50000;
        }else
        if(kodepelanggan.equals("BBBB")){
            status ="LAMA";
            biayadaftar =300000;
        }else {
            status = "Tidak ada";
            biayadaftar = 0;
        }

        txtStatus.setText(""+status);
    }//GEN-LAST:event_btnproses1ActionPerformed

    private void btnproses2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnproses2ActionPerformed
        // TODO add your handling code here:
        String jenisfilm,judulfilm;
        double nodvd,biayasewa,totalbayar;
        int jumlahhari;

        nodvd=Double.parseDouble(txtnodvd.getText());
        jumlahhari=Integer.parseInt(txtjumlahhari.getText());

        if(txtnodvd.equals("1111")){
            jenisfilm ="ACTION";
            judulfilm ="IRON MAN";
            biayasewa =25000;
        }else
        if(txtnodvd.equals("2222")){
            jenisfilm ="DRAMA";
            judulfilm ="DILAN 1990";
            biayasewa =10000;
        }else
        if(txtnodvd.equals("3333")){
            jenisfilm ="KOLOSAL";
            judulfilm ="THE LORD OF THE RINGS";
            biayasewa =15000;
        }else
        if(txtnodvd.equals("4444")){
            jenisfilm ="KOMEDI";
            judulfilm ="WARKOP REBORN";
            biayasewa =17500;
        }else
        if(txtnodvd.equals("5555")){
            jenisfilm ="HOROR";
            judulfilm ="THE NUN";
            biayasewa =25000;
        }else {
            jenisfilm ="Tidak ada";
            judulfilm ="0";
            biayasewa =0;
        }

        txtJenisFilm.setText(""+jenisfilm);
        txtJudulFilm.setText(""+judulfilm);
        txtbiayasewa.setText(""+biayasewa);

        totalbayar = biayadaftar + (biayasewa * jumlahhari);

        txtTotalBayar.setText(""+totalbayar);
    }//GEN-LAST:event_btnproses2ActionPerformed

