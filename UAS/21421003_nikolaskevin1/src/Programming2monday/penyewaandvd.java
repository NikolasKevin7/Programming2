    private void CBKodePlgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CBKodePlgActionPerformed
        // TODO add your handling code here:
       String kodepelanggan,status;
       double biayadaftar;

        kodepelanggan=String.valueOf(txtkodepelanggan.getText());
        biayadaftar=Double.parseDouble(txtbiayadaftar.getText());

        if(kodepelanggan.equals("AAAA")){
            status ="BARU";
            biayadaftar =50000;
        }else
        if(kodepelanggan.equals("BBBB")){
            status ="LAMA";
            biayadaftar =300000;
        }else {
            status = "Tidak ada";
            biayadaftar = 0;
        }

        txtStatus.setText(""+status);
    }//GEN-LAST:event_CBKodePlgActionPerformed

