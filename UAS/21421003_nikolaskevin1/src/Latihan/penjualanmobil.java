    private void cbkdmblActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbkdmblActionPerformed
        // TODO add your handling code here:
        String namamobil;
        String pilihan;
        double harga;
        
        pilihan=(String)(cbkdmbl.getSelectedItem());
        
        if(pilihan == "DX001"){
            harga = 150000;
            txtharga.setText(""+harga);
            namamobil = "Innova";
        } else if(pilihan == "CA001"){
            harga = 100000;
            txtharga.setText(""+harga);
            namamobil = "Toyota";
        } else if(pilihan == "SU003"){
            harga = 200000;
            txtharga.setText(""+harga);
            namamobil = "Pajero Sport";
        }else if(pilihan == "DN001"){
            harga = 120000;
            txtharga.setText(""+harga);
            namamobil = "Panther";
        }else if(pilihan == "GT002"){
            harga = 125000;
            txtharga.setText(""+harga);
            namamobil = "Kijang";
        }else{
            harga = 0;
            txtharga.setText(""+harga);
            namamobil = "Tidak Ada";
        }
        
        txtnamamobil.setText(namamobil); 
    }//GEN-LAST:event_cbkdmblActionPerformed

